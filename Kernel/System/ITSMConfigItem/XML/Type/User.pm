=head1 NAME

Kernel::System::ITSMConfigItem::XML::Type::User - xml backend module

=head1 SYNOPSIS

All xml functions of user objects

=head2 new()

create an object

    my $XMLTypeUserBackendObject = $Kernel::OM->Get('Kernel::System::ITSMConfigItem::XML::Type::User');

=cut

sub new {
    my ( $Type, %Param ) = @_;

    # allocate new hash for object
    my $Self = {};
    bless( $Self, $Type );

    return $Self;
}

=head2 ValueLookup()

get the xml data of a version

    my $Value = $BackendObject->ValueLookup(
        Value => 11, # (optional)
    );

=cut

sub ValueLookup {
    my ( $Self, %Param ) = @_;

    return '' if !$Param{Value};

    my $UserObject = $Kernel::OM->Get('Kernel::System::User');

    my %User = $UserObject->GetUserData(
        UserID => $Param{Value},
    );

    if (%User) {

        my $UserValue = sprintf '"%s %s" <%s>',
            $User{UserFirstname},
            $User{UserLastname},
            $User{UserEmail};

        return $UserValue;
    }

    return $Param{Value};

}

=head2 StatsAttributeCreate()

create a attribute array for the stats framework

    my $Attribute = $BackendObject->StatsAttributeCreate(
        Key  => 'Key::Subkey',
        Name => 'Name',
        Item => $ItemRef,
    );

=cut

sub StatsAttributeCreate {
    my ( $Self, %Param ) = @_;

    NEEDED:
    for my $Needed (qw(Key Name Item)) {

        next NEEDED if defined $Param{$Needed};

        $Kernel::OM->Get('Kernel::System::Log')->Log(
            Priority => 'error',
            Message  => "Need $Needed!",
        );
        return;
    }

    # create attribute
    my $Attribute = [
        {
            Name             => $Param{Name},
            UseAsXvalue      => 1,
            UseAsValueSeries => 1,
            UseAsRestriction => 1,
            Element          => $Param{Key},
            Block            => 'InputField',
        },
    ];

    return $Attribute;
}

